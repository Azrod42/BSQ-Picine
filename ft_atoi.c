int	ft_atoi(char *str)
{
	int	res;
	int	s;

	res = 0;
	s = 1;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			s = -s;
		str++;
	}
	while (*str >= 48 && *str <= 57)
	{
		res *= 10;
		res += s * (*str - 48);
		str++;
	}
	return (res);
}
